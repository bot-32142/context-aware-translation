from __future__ import annotations

from typing import TYPE_CHECKING, Protocol

if TYPE_CHECKING:
    from context_aware_translation.workflow.tasks.models import ActionSnapshot, Decision, TaskAction
    from context_aware_translation.workflow.tasks.claims import DocumentScope, ResourceClaim
    from context_aware_translation.storage.task_store import TaskRecord
    from context_aware_translation.workflow.tasks.worker_deps import WorkerDeps


class TaskTypeHandler(Protocol):
    task_type: str

    def decode_payload(self, record: "TaskRecord"): ...
    def scope(self, record: "TaskRecord", payload) -> "DocumentScope": ...
    def claims(self, record: "TaskRecord", payload) -> frozenset["ResourceClaim"]: ...
    def can(self, action: "TaskAction", record: "TaskRecord", payload, snapshot: "ActionSnapshot") -> "Decision": ...
    def can_autorun(self, record: "TaskRecord", payload, snapshot: "ActionSnapshot") -> "Decision": ...
    def validate_submit(self, book_id: str, params: dict, deps: "WorkerDeps") -> "Decision": ...
    def pre_delete(self, record: "TaskRecord", payload, deps: "WorkerDeps") -> list[str]: ...
    def build_worker(self, action: "TaskAction", record: "TaskRecord", payload, deps: "WorkerDeps"): ...
